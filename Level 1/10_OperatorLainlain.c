#include <stdio.h>

int main () {
    int a = 18;
    int b = 3;

    // operator lainnya
    printf("***OPERATOR LAINNYA***\n");
    printf("a = %d\n", a);
    printf("b = %d\n", b);
    printf("\n");
    printf("\n");

    // increment
    printf("***INCREMENT***\n");
    printf("++a = %d\n", ++a);
    printf("\n");

    // decrement
    printf("***DECREMENT***\n");
    printf("--b = %d\n", --b);
    printf("\n");

    return 0;
}
